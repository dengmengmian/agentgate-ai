//! AppError 错误码常量——集中所有面向前端的 error code 字符串。
//!
//! ## 为什么这样写
//!
//! `AppError::new("CODE_STR", message)` 散落在 219 处调用点，拼写错误（如
//! "PROVDER_API_KEY_MISSING"）只能靠 code review 发现，编译器和测试都看不到。
//! 改成 `AppError::new(codes::PROVIDER_API_KEY_MISSING, message)` 后，拼错就是
//! `unknown identifier` 编译错误。
//!
//! ## 契约约束
//!
//! 这些 code **字符串值是面向前端的契约**（前端有 `err.code === "NO_API_KEY"`
//! 这类判断），所以 const 的字面值必须和原本散落的字符串完全一致——只是把字符串
//! 重复挪到一个地方，不改语义。新增 code 必须先在前端处理逻辑里达成共识再加。
//!
//! ## 不在这里的
//!
//! - `NOT_FOUND` / `VALIDATION_ERROR` / `DATABASE_ERROR` / `NETWORK_ERROR` /
//!   `INTERNAL_ERROR`：由 AppError 的便捷构造方法 (`not_found`, `validation`,
//!   `database`, `internal`, `From<reqwest::Error>`) 内部产出，不通过 `new`。
//! - 测试 sentinel 如 `"TEST"` / `"TEST_CODE"` / `"CODE"` / `"UNKNOWN_CODE"`：
//!   仅为单测虚构，不属于面向前端契约，保留字符串。

